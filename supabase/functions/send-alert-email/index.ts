import { serve } from "https://deno.land/std@0.190.0/http/server.ts";
import { Resend } from "npm:resend@2.0.0";

const resend = new Resend(Deno.env.get("RESEND_API_KEY"));

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type",
};

interface AlertEmailRequest {
  email: string;
  location: string;
  severity: string;
  timestamp: string;
}

const handler = async (req: Request): Promise<Response> => {
  // Handle CORS preflight requests
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { email, location, severity, timestamp }: AlertEmailRequest = await req.json();

    const emailResponse = await resend.emails.send({
      from: "SheGuard Alert <alerts@resend.dev>",
      to: [email],
      subject: `ðŸš¨ SheGuard Alert - ${severity.toUpperCase()} Emergency Detected`,
      html: `
        <div style="font-family: Arial, sans-serif; max-width: 600px; margin: 0 auto; padding: 20px;">
          <div style="background: linear-gradient(135deg, #dc2626, #991b1b); color: white; padding: 20px; border-radius: 8px; margin-bottom: 20px;">
            <h1 style="margin: 0; font-size: 24px;">ðŸš¨ EMERGENCY ALERT</h1>
            <p style="margin: 10px 0 0 0; font-size: 16px;">SheGuard has detected a potential emergency</p>
          </div>
          
          <div style="background: #f9fafb; padding: 20px; border-radius: 8px; margin-bottom: 20px;">
            <h2 style="margin: 0 0 15px 0; color: #374151;">Alert Details</h2>
            <p style="margin: 5px 0;"><strong>Severity:</strong> <span style="color: #dc2626; text-transform: uppercase;">${severity}</span></p>
            <p style="margin: 5px 0;"><strong>Location:</strong> ${location}</p>
            <p style="margin: 5px 0;"><strong>Time:</strong> ${timestamp}</p>
          </div>
          
          <div style="background: #fef3c7; border-left: 4px solid #f59e0b; padding: 15px; margin-bottom: 20px;">
            <p style="margin: 0; color: #92400e;"><strong>Action Required:</strong> This alert has been automatically generated by SheGuard's AI voice detection system. Please check on the safety of the person associated with this alert.</p>
          </div>
          
          <div style="text-align: center; margin-top: 20px;">
            <p style="color: #6b7280; font-size: 14px;">
              This is an automated message from SheGuard.<br>
              If this is a real emergency, please contact local authorities immediately.
            </p>
          </div>
        </div>
      `,
    });

    console.log("Alert email sent successfully:", emailResponse);

    return new Response(JSON.stringify(emailResponse), {
      status: 200,
      headers: {
        "Content-Type": "application/json",
        ...corsHeaders,
      },
    });
  } catch (error: any) {
    console.error("Error in send-alert-email function:", error);
    return new Response(
      JSON.stringify({ error: error.message }),
      {
        status: 500,
        headers: { "Content-Type": "application/json", ...corsHeaders },
      }
    );
  }
};

serve(handler);